    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
      try {
        // Validasi apakah semua field terisi
        if (txtNamaPelanggan.getText().isEmpty() || txtNoHP.getText().isEmpty() || txtAlamat.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Data Pelanggan belum diisi", "Gagal ubah data", 2);
            return; // Kembali jika ada field yang kosong
        }
        
        // Query untuk update data pelanggan
        String queryUbah = "UPDATE pelanggan SET nama_pelanggan = ?, no_telp = ?, alamat = ? WHERE id = ?";
        pst = conn.prepareStatement(queryUbah);
        
        // Mengisi parameter query berdasarkan input pengguna
        pst.setString(1, txtNamaPelanggan.getText());
        pst.setString(2, txtNoHP.getText());
        pst.setString(3, txtAlamat.getText());
        pst.setString(4, id); // Mengisi ID pelanggan yang ingin diubah
        
        // Eksekusi query
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data pelanggan berhasil diubah", "Sukses", 1);
        
        // Memperbarui data di tabel dan membersihkan input setelah update
        tampilData();
        bersih();
        
        } catch (SQLException ex) {
            Logger.getLogger(PelangganView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            if(txtNamaPelanggan.getText().isEmpty() ||txtNoHP.getText().isEmpty()
                    || txtAlamat.getText().isEmpty()){
                JOptionPane.showConfirmDialog(null,"data Pelanggan belum di isi",
                        "Gagal tambah data", 2);
            }else{
                int konfirmasi = JOptionPane.showConfirmDialog(null,"Hapus data pelanggan ini?", "Konfirmasi Hapus?", 0);
                if(konfirmasi == 0){
                String queryTambah = "delete from pelanggan where id=?;";
                pst = conn.prepareStatement(queryTambah);
                pst.setString(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data pelanggan berhasil dihapus", "Sukses", 1);
                tampilData();
                bersih();
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(PelangganView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

