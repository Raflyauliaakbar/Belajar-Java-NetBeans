    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
        // Validasi apakah semua field terisi
        if(txtNamaPelanggan.getText().isEmpty() || txtNoHP.getText().isEmpty() || txtAlamat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Pelanggan belum diisi", "Gagal tambah data", JOptionPane.WARNING_MESSAGE);
            return; // Kembali jika ada field yang kosong
        }

        // Query untuk menambahkan data pelanggan
        String queryTambah = "INSERT INTO pelanggan (nama_pelanggan, alamat, no_telp) VALUES (?, ?, ?)";
        pst = conn.prepareStatement(queryTambah);

        // Mengisi parameter sesuai urutan kolom dalam tabel
        pst.setString(1, txtNamaPelanggan.getText());
        pst.setString(2, txtAlamat.getText()); // Urutan sesuai tabel: alamat di posisi kedua
        pst.setString(3, txtNoHP.getText());   // no_telp di posisi ketiga

        // Eksekusi query
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data pelanggan berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);

        // Memperbarui data di tabel dan membersihkan input setelah menambah data
        tampilData();
        bersih();

    } catch (SQLException ex) {
        Logger.getLogger(PelangganView.class.getName()).log(Level.SEVERE, null, ex);
    }
      
    }//GEN-LAST:event_btnTambahActionPerformed

