//Method untuk menampilkan data
    private void tampilData(){
        try {
            String[] judul = {"ID", "Nama Pelanggan", "Alamat", "No HP"};
            DefaultTableModel dtm = new DefaultTableModel(null, judul);
            jTable1.setModel(dtm);
            String sql = "select * from pelanggan";
            if(!txtCariPelanggan.getText().isEmpty()){
                sql = "SELECT * FROM pelanggan WHERE nama_pelanggan LIKE '%" + txtCariPelanggan.getText() + "%';";
            }
            pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                String[] data = {rs.getString(1),
                    rs.getString(2), rs.getString(3), rs.getString(4)};
                dtm.addRow(data);
            } } catch (SQLException ex) {
            Logger.getLogger(PelangganView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

